a = [1, 2, 3, 4]    # Ejemplo de lista

print(a)            # Imprime la lista

a.append(5)         # Añade un elemento al final de la lista

print(a)            # Imprime la lista actualizada

a.pop()             # Elimina el último elemento de la lista

print(a)            # Imprime la lista después de eliminar el último elemento

print(a[0])         # Imprime el primer elemento de la lista

b = [1, 1.2, (1, 2, 3)] # Lista con diferentes tipos de datos

print(b[2][1])      # Imprime el segundo elemento de la tupla dentro de la lista