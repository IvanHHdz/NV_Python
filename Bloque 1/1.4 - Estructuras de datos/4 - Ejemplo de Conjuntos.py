a = {1, 2, 4, 5, 0} # Ejemplo de conjunto

print(a)            # Imprime el conjunto

print(3 in a)       # Comprueba si el número 3 está en el conjunto

a.add(3)            # Añade el número 3 al conjunto

print(a)            # Imprime el conjunto actualizado

print(3 in a)       # Comprueba nuevamente si el número 3 está en el conjunto

a.remove(1)         # Elimina el número 1 del conjunto

print(a)            # Imprime el conjunto después de eliminar el número 1

a.add("0")          # Añade el string "0" al conjunto

print(a)            # Imprime el conjunto después de añadir el string "0"