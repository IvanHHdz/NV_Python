# Declaramos variables con =

# Las variables pueden ser de diferentes tipos, como enteros, flotantes, cadenas, etc.
# En Python, no es necesario declarar el tipo de variable, se infiere automáticamente

variable = 5
variable2 = 2

# Esto es un comentario, no se ejecuta
# print(variable)

# Podemos realizar operaciones con las variables
print(variable + variable2)
print(variable - variable2)
print(variable * variable2)
print(variable / variable2)

# También tenemos el módulo y la división entera
print(variable % variable2)  # Módulo
print(variable // variable2)  # División entera